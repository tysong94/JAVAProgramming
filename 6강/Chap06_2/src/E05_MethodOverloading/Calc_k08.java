package E05_MethodOverloading;

public class Calc_k08 {

	public int sum_k08(int a, int b) {
		return a + b;
	}
	
	public int sum_k08(int a, int b, int c) {
		return a + b + c;
	}
	
	public int sum_k08(int a, int b, int c, int d) {
		return a + b + c + d;
	}
	
	public double sum_k08(double a, double b) {
		return a + b;
	}

}
